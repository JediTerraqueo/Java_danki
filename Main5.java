package aula;

public class Main5 {
	public static void main(String[] args) {
		
		//Array normal
		String[] nome = new String[5];
		nome[0] = "Girombossauro";
		nome[1] = "Saco Grande";
		System.out.println(nome[0]+" "+nome[1]);
		
		//Array Multidimensional
		String[][] var2 = new String[5][5];
		var2[0][0] = "Felipe";
		System.out.println(var2[0][0]);
		
		
	}
	
}
